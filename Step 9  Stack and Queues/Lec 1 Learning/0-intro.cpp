/*
                    STACK
                
            LIFO -> Last In First Out

            Stack is a data structure that follows the Last In First Out (LIFO) rule.


            push - to add a value at last in stack 
            pop - to remove a last value from stack 
            top -> access the top / last value of stack
*/


/*

                    QUEUE
                
            FIFO -> First In First Out

            Queue is a data structure that follows the First In First Out (FIFO) rule.

            push - to add a value at last in queue 
            pop - to remove a first value from queue 
            top -> access the first value of queue
*/